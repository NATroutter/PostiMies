package net.natroutter.postimies.Utilities.objects;

public class StringArg {

    String placeholder;
    String value;

    public StringArg(String placeholder, String value) {
        this.placeholder = placeholder;
        this.value = value;
    }

    public String getPlaceholder() {
        return placeholder;
    }

    public String getValue() {
        return value;
    }


}
